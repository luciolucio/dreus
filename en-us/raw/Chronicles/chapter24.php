                <td>
                  <p>Now, Gord liked to sell games from Japan as Japan gets a 
                  lot of curiously odd games that never come out in North America.&nbsp; 
                  Sadly, North American gamers generally only want new games that they have 
                  already played before and like to complain that there are no 
                  curiously odd games.</p>
                  <p>Anyway, there is quite the market for importing games from 
                  Japan as a number of people actually want the curiously odd 
                  games.</p>
                  <p>While the legality of a territorial restriction on a game 
                  console is questionably dubious as it is clearly a violation 
                  of laws that specifically address and prohibit the encumbrance 
                  of trade, that is a discussion for another time.</p>
                  <p>Long story short, you can't play games from Japan on your 
                  North American PlayStation because Sony hates you.&nbsp; 
                  </p>
                  <p>To get around machine encoded lockout, you have to consider 
                  the options.&nbsp; First, buy a machine from Japan.&nbsp; The 
                  &quot;not so cheap&quot; way.</p>
                  <p>Or the cheap way, have a small chip installed into your 
                  PlayStation that circumvents the territorial protection of the 
                  PlayStation, and thereby allowing you to play games from any 
                  region.</p>
                  <p>The most common variant of &quot;mod chips&quot; are the ones that 
                  also nullify the copy protection scheme in the machine.&nbsp; 
                  For some strange reason, this is a very popular attraction.</p>
                  <p>However, as a game store owner, Gord likes the first reason 
                  and not the second one.&nbsp; A rather uncommon mod chip that 
                  he stocked was a mod chip that did only territorial protection 
                  removal.&nbsp; Most people would assume that mod chips by 
                  default removed copy protection.&nbsp; This provided many, 
                  many hours of entertainment.</p>
                  <p>&quot;Do you sell mod chips?&quot;</p>
                  <p><i>&quot;Yes, I sell mod chips that let you play games from 
                  Japan.&quot;</i></p>
                  <p>&quot;Oh, great!&nbsp; I've been wanting to get one for a long 
                  time.&nbsp; I'll go get my machine.&quot;</p>
                  <p>Customer leaves, brings in PlayStation.</p>
                  <p>Gord gets mod chip installed.</p>
                  <p>Customer picks up machine.</p>
                  <p>Customer returns angry.&nbsp; 
                  </p>
                  <p>&quot;It doesn't play back-ups!&quot;</p>
                  <p><i>&quot;It also makes for a lousy speed dump and it isn't a web 
                  shooter.&nbsp; Catches criminals like flies, my ass!&quot;</i></p>
                  <p>&quot;I wanted to play back-ups.&quot;</p>
                  <p><i>&quot;I want to be Spiderman.&quot;</i></p>
                  <p>&quot;You said it would play back-ups!&quot;</p>
                  <p><i>&quot;No, I said it would play imports.&quot;</i></p>
                  <p>&quot;You ripped me off.&quot;</p>
                  <p><i>&quot;No, I sold you exactly what I said I would.&quot;</i></p>
                  <p>&quot;Give me my money back.&quot;</p>
                  <p><i>&quot;Not seeing a positive angle for me in your plan.&quot;</i></p>
                  <p>&quot;I'm never coming back!&quot;</p>
                  <p><i>&quot;Door's to your left.&quot;</i></p>
                  <p>Or for the sake of variety, as this type of conversation 
                  would happen rather regularly, he would actually offer the 
                  refund.</p>
                  <p><i>&quot;Very well, I'll give you a refund on the cost of the 
                  chip.&quot;</i></p>
                  <p>&lt;Gord would remove chip&gt;</p>
                  <p><i>&quot;Here your machine, and here's your $10.&quot;</i></p>
                  <p>&quot;Hey!&nbsp; I paid $50.&quot;</p>
                  <p><i>&quot;Yes, $10 for the chip, and $40 for installation.&nbsp; 
                  Taxes included.&quot;</i></p>
                  <p>&quot;I want all my money back!&quot;</p>
                  <p><i>&quot;Sorry, labour is non-refundable.&quot;</i></p>
                  <p>Alas, this would soon become the favourite way of handling 
                  the situation.&nbsp; And the best part would be when they 
                  would ask for the chip to be installed again.</p>
                  <p><i>&quot;Very well, that be $40 for installation.&nbsp; Payable 
                  in advance this time.&quot;</i></p>
                  <p>&quot;You're a rip-off!&quot;</p>
