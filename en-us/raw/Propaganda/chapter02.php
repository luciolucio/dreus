                    WWF Attitude.&nbsp; Bad game, and far too many copies 
                    produced for the demand.&nbsp; But over the course of a 
                    week, Gord had three backorders for it.&nbsp; Probably 
                    because it was a third the price of Wrestlemania 2000 back 
                    when it first came out.<p>Anyway, Gord ordered three copies 
                    as part of his order.&nbsp; And in the end, got eleven.&nbsp; 
                    He wasn't charged for the extra eight copies, but he 
                    honestly did not want them.&nbsp; So he phoned the 
                    distributor up.</p>
                    <p>The distributor didn't want them back.&nbsp; It was going 
                    to cost more in courier costs than it was worth to get them 
                    back.&nbsp; They told Gord to keep them.</p>
                    <p>So Gord did.&nbsp; And created a sign.</p>
                    <p>WWF Attitude!&nbsp; Gaming has never been this good, and 
                    it will never be this good again!&nbsp; Forget the lies that 
                    you may have heard that this is a poor game.&nbsp; Those are 
                    from WCW fans!&nbsp; They're just angry because there never 
                    was a WCW game as good as this!</p>
                    <p>But my friends, gaming like this doesn't come without a 
                    price!&nbsp; What will that price be you ask?&nbsp; Why, 
                    it's make up your own price day!&nbsp; That's right!&nbsp; 
                    You name the price!&nbsp; Be warned though, one of two 
                    things will happen.&nbsp; Either you'll get the game at the 
                    price you offer, or you'll get slapped for such an inane 
                    offer.</p>
